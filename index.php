                    bedreiging voor onze economie! Hoe deze cryptocurrencies werken en waarom deze zo populair zijn
                    geworden zal je alles horen van Bruno Ryckaert.</p>
            </section>
            <section style="clear: both; margin-bottom: 100px;">
                <h2>Mitchell Verbruggen</h2>
                <span class="image left"><img src="images/mitchell.png" alt=""/></span>
                <p><b>VoIP</b></p>
                <p>Een kleine 44 jaar geleden heeft de opkomst van de mobiele telefoon onze manier van communicatie
                    gerevolutioneerd, maar de dag van vandaag staan wij verder dan ooit in onze manier van communicatie.
                    Voice over IP, een algemene term voor het telefoneren over het internet, is een fenomeen geworden
                    dat niet meer weg te denken is! Mitchell Verbruggen zal ons meer vertellen over wat VoIP is, wat de
                    verschillen zijn met klassieke telefonie en wat de toekomst ons brengt.</p>
